                $("#password-not-same").show();
            }else{
                if($("#password").val().length < 8){
                    $("#password-not-same").html("<strong>Password harus setidaknya berisi 8 Karakter</strong>");
                    $("#password-not-same").show();
                }else{
                    $("#password-not-same").hide();
                    $("#signup").prop('disabled', false);
                }
            }
            
        });
	});
